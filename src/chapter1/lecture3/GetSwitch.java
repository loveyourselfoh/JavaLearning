package chapter1.lecture3;

public class GetSwitch {
    public static void main(String[] args){
        int week=5;
        switch(week){
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wendnesday");
                break;
            default:
                System.out.println("Sorry,I don't know.");
        }
    }

}
